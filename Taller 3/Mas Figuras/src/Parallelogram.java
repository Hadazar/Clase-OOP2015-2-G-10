
public class Parallelogram extends Quadrilateral {
	
	public double base;
	public double hight;
	public double side;
	
	public void setAttributes(){
		
	}

	public double getBase(){
		return base;
	}
	

	public double getHight(){
		return hight;
	}
	

	public double getSide(){
		return side;
	}
	
	public double area (){
		return base * hight;
	}
}
